                        href="url('http://www.dpd.com')">DPD SK</a>, kde si slovenský zákazník vybere zemi „Slovakia“, klikne
                    na šipku a poté se dostane na stránky DPD SK, dále klikne na „Vaše Depo“, kde se po doplnění PSČ
                    dostanete na příslušné depo v místě jeho bydliště, kde je uveden e-mail kontakt, nebo na tel. 244
                    451 419.</li>
