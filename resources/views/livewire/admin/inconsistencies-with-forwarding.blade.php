            <h2>Postup pro balíky:</h2><br>
            V případě zboží, ať už je přepravní obal podle Vás poškozen, nebo ne, důležité je se zbožím již
            nemanipulovat (netrhávat přepravní pásky apod.) a vždy škodu nahlásit na přepravní společnosti. <br><br>
            <strong>Doba nutná k nahlášení + kontakty přepravců, kde hlásit poškození</strong><br>
            <ul>
                <li>u PPL - musí být podána výhrada u viditelně poškozených balíků okamžitě do přepr.listu, popř. vůbec
                    nepřebírat, u skrytého poškození nahlášení nejpozději do 3 dnů včetně data převzetí,a poté nahlášení
                    na - <a href="{{ url('http://www.ppl.cz/reklamace.asp')}}">adresu</a> nebo tel. na 844 775 775. Neprodleně
                    sepište škodový protokol. <br><a href="/staticcontent/files/op_ppl.pdf">Obchodní podmínky
                        přepravce</a></li>
                <li>u DPD - musí být podána výhrada u viditelně poškozených balíků okamžitě do přepr.listu, popř. vůbec
                    nepřebírat, u skrytého poškození nahlášení nejpozději do 3. DNE včetně data převzetí, a poté
                    nahlášení na <a href="{{url('http://public.dpd.cz/ocm/')}}">adresu</a>. Neprodleně sepište škodový protokol.
                    Pokud nemáte tuto možnost, volejte na tel. 841 700 700<br><a
                        href="/staticcontent/files/op_dpd.pdf">Obchodní podmínky přepravce</a></li>
